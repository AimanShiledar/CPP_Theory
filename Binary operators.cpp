// cpp program to demonstrate the Binary Operator

#include <iostream>
using namespace std;

int main()
{
int a = 10;
int b = 28;

//Addition Operator
cout<<"a+b = "<<(a + b)<<endl;

//Subtraction Operator
cout<<"a-b = "<<(a-b)<<endl;

//Multiplication Operator
cout<<"a*b = "<<(a*b)<<endl;

//Division Operator
cout<<"a/b = "<<(a/b)<<endl;

//Modules Operator
cout<<"a%b = "<<(a%b)<<endl;

 return 0;
}